    private void compress(String directory) {
        List<String> files = new ArrayList<String>();
        String[] selectedFiles = destinationArea.getText().split("\n");
        for(int i = 0; i < selectedFiles.length; i++) {
            files.add(selectedFiles[i]);
        }
        ZipCompress zip = new ZipCompress(files, directory);
        zip.setVisible(true);
    }
    
