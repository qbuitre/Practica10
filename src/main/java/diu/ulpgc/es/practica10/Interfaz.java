    private void closeAppMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_closeAppMenuActionPerformed
        int ans = JOptionPane.showConfirmDialog(this, "Do you want to close the database connection?", "Exit", JOptionPane.YES_NO_OPTION);
        if(ans == JOptionPane.YES_OPTION) {
            this.dispose();
        }        
    }//GEN-LAST:event_closeAppMenuActionPerformed

    private void chooseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseButtonActionPerformed
        int op = fc.showOpenDialog(this);
        if(JFileChooser.APPROVE_OPTION == op){
            filesModel.removeAllElements();
            destinationArea.setText("");
            destinationField.setText("");
            compressButton.setEnabled(false);
            File file = fc.getSelectedFile();
            sourceField.setText(file.getPath());
            for (File f : file.listFiles()) {
                if(f.isFile()) {
                    filesModel.addElement(f.getPath());
                }
            }
            sourceList.setModel(filesModel);
        }        
    }//GEN-LAST:event_chooseButtonActionPerformed

    private void clearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearButtonActionPerformed
        sourceList.clearSelection();
        destinationArea.setText("");
    }//GEN-LAST:event_clearButtonActionPerformed

    private void compressButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_compressButtonActionPerformed
        int op = fc.showSaveDialog(this);
        if(JFileChooser.APPROVE_OPTION == op) {
            destinationField.setText(fc.getSelectedFile().getPath());
            compress(fc.getSelectedFile().getPath());
        }        
    }//GEN-LAST:event_compressButtonActionPerformed

    private void sourceListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_sourceListValueChanged
        String s = "";
        if(evt.getValueIsAdjusting()){
            int[] selectedIndices = sourceList.getSelectedIndices();
            for (int index : selectedIndices) {
                s += filesModel.getElementAt(index).toString() + "\n";
                destinationArea.setText(s);
                compressButton.setEnabled(true);
            }
        }        
    }//GEN-LAST:event_sourceListValueChanged

